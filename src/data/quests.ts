export type QuestType = "daily" | "weekly";
export type QuestAction = "catch_fish" | "catch_rarity" | "earn_coins" | "cast" | "sell_items" | "work" | "catch_junk" | "use_potion" | "give_coins" | "win_duel" | "buy_lottery" | "complete_trade";

export interface QuestDef {
  id: string;
  name: string;
  description: string; // use {goal} placeholder
  emoji: string;
  action: QuestAction;
  actionFilter?: string; // e.g. rarity name for catch_rarity
  type: QuestType;
  goalRange: [number, number]; // random goal between min-max
  rewards: {
    coins?: [number, number];
    xp?: [number, number];
    gems?: [number, number];
  };
}

